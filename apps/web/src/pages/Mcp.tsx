import React, { useState } from "react";
import { Zap, Plus, LayoutGrid, List, AlertTriangle } from "lucide-react";
import type { Scope } from "@skilldock/shared";
import { useMcpList, useMcpAgents, useMcpAdd } from "../hooks/useMcp";
import { ScopeToggle } from "../components/ui/ScopeToggle";
import { EmptyState } from "../components/ui/EmptyState";
import { CommandResultView } from "../components/ui/CommandResultView";
import { ConfirmDialog } from "../components/ui/ConfirmDialog";
import { useLocale } from "../contexts/LocaleContext";

export function Mcp({ onTaskStart }: { onTaskStart: (tid: string, title: string) => void }) {
  const [scope, setScope] = useState<Scope>("project");
  const { data: mcpListData, isLoading: isListLoading } = useMcpList(scope);
  const { data: agentsData, isLoading: isAgentsLoading } = useMcpAgents();
  const addMutation = useMcpAdd();
  const { t } = useLocale();

  const [confirmState, setConfirmState] = useState<{
    isOpen: boolean;
    title: string;
    message: string[];
    confirmLabel?: string;
    onConfirm: () => void;
  } | null>(null);

  // Form states
  const [target, setTarget] = useState("");
  const [name, setName] = useState("");

  const handleAddMcp = (e: React.FormEvent) => {
    e.preventDefault();
    if (!target.trim()) return;

    setConfirmState({
      isOpen: true,
      title: t("mcp.addServerTitle"),
      message: [
        t("mcp.addServerMessage", { target }),
        name ? t("mcp.addServerName", { name }) : t("mcp.nameAutoGenerated"),
        t("mcp.addServerScope", { scope })
      ],
      confirmLabel: t("mcp.addServerButton"),
      onConfirm: async () => {
        const res = await addMutation.mutateAsync({
          target,
          name: name || undefined,
          scope,
        });
        onTaskStart(res.taskId, t("mcp.addingMcp", { target }));
        setTarget("");
        setName("");
        setConfirmState(null);
      }
    });
  };

  return (
    <div className="space-y-8 animate-in fade-in slide-in-from-bottom-4 duration-500">
      {addMutation.error && (
        <div className="p-4 rounded-xl bg-danger/10 border border-danger/30 text-danger text-sm flex items-center gap-3">
          <AlertTriangle size={16} />
          <span>{addMutation.error instanceof Error ? addMutation.error.message : t("common.operationFailed")}</span>
        </div>
      )}
      <header className="flex flex-col md:flex-row md:items-center justify-between gap-6">
        <ScopeToggle label={t("common.scope")} value={scope} onChange={setScope} />

        <form onSubmit={handleAddMcp} className="flex gap-2">
          <input
            type="text"
            placeholder={t("mcp.targetPlaceholder")}
            value={target}
            onChange={e => setTarget(e.target.value)}
            className="text-xs py-1.5 w-64 bg-surface-800 border-border rounded-lg"
          />
          <input
            type="text"
            placeholder={t("mcp.namePlaceholder")}
            value={name}
            onChange={e => setName(e.target.value)}
            className="text-xs py-1.5 w-40 bg-surface-800 border-border rounded-lg"
          />
          <button
            type="submit"
            disabled={!target.trim() || addMutation.isPending}
            className="px-4 py-1.5 bg-accent text-white rounded-lg text-xs font-bold flex items-center gap-2 hover:opacity-90 disabled:opacity-50"
          >
            <Plus size={14} />
            {t("mcp.addServerButton")}
          </button>
        </form>
      </header>

      <div className="grid grid-cols-1 xl:grid-cols-2 gap-8">
        {/* MCP List Section */}
        <section className="space-y-4">
          <div className="flex items-center gap-3">
            <List size={20} className="text-accent-light" />
            <h3 className="font-bold tracking-tight uppercase text-xs tracking-widest text-text-muted">{t("mcp.configuredServers")}</h3>
          </div>

          {isListLoading ? (
            <div className="h-64 rounded-2xl bg-surface-800 border border-border animate-pulse" />
          ) : mcpListData ? (
            <CommandResultView
              title={t("mcp.configuredServers")}
              description={t("mcp.configuredServersDesc")}
              result={mcpListData}
              scope={scope}
            />
          ) : (
            <EmptyState title={t("mcp.noMcpConfig")} message={t("mcp.unableToFetch")} />
          )}
        </section>

        {/* Agents Section */}
        <section className="space-y-4">
          <div className="flex items-center gap-3">
            <LayoutGrid size={20} className="text-warning" />
            <h3 className="font-bold tracking-tight uppercase text-xs tracking-widest text-text-muted">{t("mcp.availableAgents")}</h3>
          </div>

          {isAgentsLoading ? (
            <div className="h-64 rounded-2xl bg-surface-800 border border-border animate-pulse" />
          ) : agentsData ? (
            <CommandResultView
              title={t("mcp.availableAgents")}
              description={t("mcp.availableAgentsDesc")}
              result={agentsData}
            />
          ) : (
            <EmptyState title={t("mcp.noAgentsFound")} message={t("mcp.couldNotRetrieve")} />
          )}
        </section>
      </div>

      {confirmState && (
        <ConfirmDialog
          isOpen={confirmState.isOpen}
          title={confirmState.title}
          message={confirmState.message}
          confirmLabel={confirmState.confirmLabel}
          onConfirm={confirmState.onConfirm}
          onCancel={() => setConfirmState(null)}
        />
      )}
    </div>
  );
}
